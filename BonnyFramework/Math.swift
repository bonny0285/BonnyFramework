import Foundation

public class Math {
    
    private init() {}
    
    
    public static func add (first number1: Int, Second number2: Int) -> Int {
        return number1 + number2
    }
    
    public static func sub (first number1: Int, Second number2: Int) -> Int {
        return number1 - number2
    }
    
    public static func moltiply (first number1: Int, Second number2: Int) -> Int {
        return number1 * number2
    }
    
    public static func divide (first number1: Int, Second number2: Int) -> Int {
        return number1 / number2
    }
    
}
